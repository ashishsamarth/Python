# Method to convert a string to a list
@staticmethod
# Argument to this method: string
def str_to_list(_my_string):
    # declare a empty list
    data_type_align = []
    for _ in _my_string:
        # Append elements of the string to list
        data_type_align.append(_)
    # Return type is a list
    return data_type_align
